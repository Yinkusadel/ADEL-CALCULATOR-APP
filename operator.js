const operate = (operator, num1, num2) => {
  switch (operator) {
    case '+':
      return num1 + num2;
    case '-':
      return num1 - num2;
    case '*':
      return num1 * num2;
    case '/':
      if (num2 !== 0) {
        return num1 / num2;
      }
      return 'Division by zero is not allowed';

    default:
      return 'pick operator';
  }
};


operate();
